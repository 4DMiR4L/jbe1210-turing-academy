package az.turingacademy.module02;

public class Task05_03 {
    public static void main(String[] args) {

        System.out.println("Böyük hərflər:");
        for (char Az = 'A'; Az <= 'Z'; Az++) {
            System.out.print(Az + " ");
        }
        System.out.println("\n");
        System.out.println("Kiçik hərflər:");
        for (char Az = 'a'; Az <= 'z'; Az++) {
            System.out.print(Az + " ");
        }
    }
}
